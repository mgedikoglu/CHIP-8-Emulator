Chip8::Chip8() : pc{0x200}, opcode{0}, index_register{0}, sp{0}
{
  // Clear display
  // Clear stack
  // Clear registers V0-VF
  // Clear memory

  // Load fontset
  for (int i = 0; i < 80; ++i) // 0x50
    memory[i] = chip8_fontset[i];

  // Reset timers
}

void Chip8::emulate_cycle()
{
  fetch();
  execute();

  update_timer();
}

void Chip8::update_timer()
{
  if (delay_timer > 0)
  {
    --delay_timer;
  }
  if (sound_timer > 0)
  {
    if (sound_timer == 1)
      std::cout << "BEEP!\n";
    --sound_timer;
  }
}

unsigned short Chip8::fetch()
{
  return memory[pc] << 8 | memory[pc + 1];
}